# Loop up index
fruits = ['Banana', 'Apple', 'Groups']

print('loop up via index value')
for index in range(len(fruits)):
    print(fruits[index])

list1 = [1, 2, 3, "Python"]

print('loop up index')
for i in list1:
    print(i)

# Loop item
print('loop up item')
for fruit in fruits:
    print(fruit)
